  /**
   * 验证邮箱域名格式
   */
  validateEmailDomain(email: string, universityDomains: string[]): boolean {
    if (!email || !email.includes('@')) {
      return false;
    }

    const domain = email.split('@')[1]?.toLowerCase();
    if (!domain) {
      return false;
    }

    // 检查是否匹配大学域名
    return universityDomains.some(uniDomain => 
      domain === uniDomain.toLowerCase() || domain.endsWith('.' + uniDomain.toLowerCase())
    );
  }

  /**
   * 验证大学邮箱格式（简化版：只检查是否包含.edu）
   */
  validateChineseUniversityEmail(email: string): {
    isValid: boolean;
    suggestion?: string;
  } {
    if (!email || !email.includes('@')) {
      return { 
        isValid: false,
        suggestion: 'Please enter a valid email address'
      };
    }

    const domain = email.split('@')[1]?.toLowerCase();
    if (!domain) {
      return { 
        isValid: false,
        suggestion: 'Invalid email format'
      };
    }

    // 只检查是否包含.edu
    const isEduEmail = domain.includes('.edu');
    
    if (isEduEmail) {
      return { 
        isValid: true,
        suggestion: 'Valid education email'
      };
    }

    return { 
      isValid: false,
      suggestion: 'Please use your university email (must contain .edu)'
    };
  }

